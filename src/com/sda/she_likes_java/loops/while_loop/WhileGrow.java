package com.sda.she_likes_java.loops.while_loop;

public class WhileGrow {
    public static void main(String[] args) {
        int kidAge;
        kidAge = 1;
        while (kidAge <= 18)
        {
            System.out.println(kidAge);
            kidAge++;
        }
        System.out.println("party time!");
    }
}
