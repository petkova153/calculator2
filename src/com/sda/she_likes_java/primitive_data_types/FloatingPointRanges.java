package com.sda.she_likes_java.primitive_data_types;

public class FloatingPointRanges {
    public static void main(String[] args) {
        System.out.println("Floats:");
        System.out.println("Min float nr is: " + Float.MIN_VALUE);
        System.out.println("Max float nr is: " + Float.MAX_VALUE);

        System.out.println("Doubles:");
        System.out.println("Min double nr is: " + Double.MIN_VALUE);
        System.out.println("Max double nr is: " + Double.MAX_VALUE);
    }
}
